fun system() {
    // Função para receber o nickname de um jogador
    fun receberNickname(jogador: Int): String {
        print("Jogador $jogador, digite seu nickname: ")
        return readLine() ?: "Jogador$jogador"  // Se o jogador não digitar nada, usa um valor padrão
    }

    // Função para receber o nome do time de um jogador
    fun receberNomeTime(jogador: Int): String {
        print("Jogador $jogador, digite o nome do seu time: ")
        return readLine() ?: "Time$jogador"  // Se o jogador não digitar nada, usa um valor padrão
    }

    // Função para escolher os Pokémon para o time
    fun escolherPokemons(jogador: String): List<String> {
        println("\n$jogador, escolha seus 3 Pokémon para o time!")
        val pokemons = listOf("Charmander", "Bulbasaur", "Pikachu", "Squirtle", "Jigglypuff")
        val pokemonsEscolhidos = mutableListOf<String>()

        for (i in 1..3) {
            println("Escolha o Pokémon $i:")
            pokemons.forEachIndexed { index, pokemon ->
                println("${index + 1}. $pokemon")
            }
            val escolha = readLine()?.toIntOrNull()

            if (escolha != null && escolha in 1..pokemons.size) {
                val pokemonEscolhido = pokemons[escolha - 1]
                if (pokemonEscolhido !in pokemonsEscolhidos) {
                    pokemonsEscolhidos.add(pokemonEscolhido)
                } else {
                    println("Você já escolheu esse Pokémon. Escolha outro!")
                       // Decrementa para tentar novamente
                }
            } else {
                println("Opção inválida! Escolha um número de 1 a ${pokemons.size}.")
                  // Decrementa para tentar novamente
            }
        }
        return pokemonsEscolhidos
    }

    // Função para calcular a vantagem entre tipos
    fun calcularVantagem(tipo1: String, tipo2: String): Int {
        // Mapas de vantagens de tipo (1 -> Vantagem para o Pokémon 1, -1 -> Vantagem para o Pokémon 2, 0 -> Empate)
        val vantagens = mapOf(
            "Fogo" to listOf("Grama", "Gelo", "Inseto", "Metálico"), // Fogo vence Grama, Gelo, Inseto e Metálico
            "Água" to listOf("Fogo", "Terra", "Pedra"),  // Água vence Fogo, Terra e Pedra
            "Grama" to listOf("Água", "Terra", "Pedra"), // Grama vence Água, Terra e Pedra
            "Elétrico" to listOf("Água", "Voador"), // Elétrico vence Água e Voador
            "Voador" to listOf("Inseto", "Planta", "Lutador") // Voador vence Inseto, Planta e Lutador
        )

        // Definindo a vantagem entre os tipos
        if (vantagens[tipo1]?.contains(tipo2) == true) {
            return 1  // Tipo 1 vence
        } else if (vantagens[tipo2]?.contains(tipo1) == true) {
            return -1 // Tipo 2 vence
        }
        return 0  // Empate
    }

    // Função para determinar o vencedor de uma batalha entre dois Pokémon
    fun batalhar(pokemon1: String, pokemon2: String): Int {
        // Mapas de tipos para cada Pokémon
        val tipos = mapOf(
            "Charmander" to "Fogo",
            "Bulbasaur" to "Grama",
            "Pikachu" to "Elétrico",
            "Squirtle" to "Água",
            "Jigglypuff" to "Fada"
        )

        val tipo1 = tipos[pokemon1] ?: ""
        val tipo2 = tipos[pokemon2] ?: ""

        return calcularVantagem(tipo1, tipo2)
    }

    // Recebendo os nicknames e nomes dos times
    val nickname1 = receberNickname(1)
    val nickname2 = receberNickname(2)

    val time1 = receberNomeTime(1)
    val time2 = receberNomeTime(2)

    // Escolhendo os Pokémon para cada time
    val pokemonsTime1 = escolherPokemons(nickname1)
    val pokemonsTime2 = escolherPokemons(nickname2)

    // Exibindo os Pokémon escolhidos para cada time
    println("\n$nickname1, seu time ($time1): $pokemonsTime1")
    println("$nickname2, seu time ($time2): $pokemonsTime2")

    // Batalhas entre os Pokémon de cada time
    for (i in 0..2) { // Realizando batalhas entre os 3 Pokémon de cada time
        val pokemon1 = pokemonsTime1[i]
        val pokemon2 = pokemonsTime2[i]

        println("\nBatalha entre $pokemon1 e $pokemon2:")
        val resultado = batalhar(pokemon1, pokemon2)

        // Exibindo o resultado da batalha
        when (resultado) {
            1 -> println("\n$nickname1 venceu com $pokemon1!")
            -1 -> println("\n$nickname2 venceu com $pokemon2!")
            0 -> println("\nEmpate entre $pokemon1 e $pokemon2!")
        }
    }

    // Determinando o vencedor final
    val vitorias1 = pokemonsTime1.count { batalhar(it, pokemonsTime2[pokemonsTime1.indexOf(it)]) == 1 }
    val vitorias2 = pokemonsTime2.count { batalhar(it, pokemonsTime1[pokemonsTime2.indexOf(it)]) == -1 }

    println("\nResultado final:")
    when {
        vitorias1 > vitorias2 -> println("$nickname1 venceu a batalha com $vitorias1 vitórias!")
        vitorias2 > vitorias1 -> println("$nickname2 venceu a batalha com $vitorias2 vitórias!")
        else -> println("A batalha terminou em empate com $vitorias1 vitórias para cada time!")
    }
}
